namespace OfdrwNet.Layout.Element.Canvas
{
    /// <summary>
    /// 文本内容的当前对齐方式
    /// 
    /// 作者: 权观宇
    /// 起始时间: 2020-05-10 18:05:47
    /// </summary>
    public enum TextAlign
    {
        /// <summary>
        /// 文本在指定的位置开始
        /// </summary>
        Start,

        /// <summary>
        /// 文本在指定的位置结束。
        /// </summary>
        End,

        /// <summary>
        /// 文本的中心被放置在指定的位置
        /// </summary>
        Center,

        /// <summary>
        /// 文本左对齐
        /// </summary>
        Left,

        /// <summary>
        /// 文本右对齐
        /// </summary>
        Right
    }
}